public class EVA2_15_ACUMULADORES {


    public static void main(String[] args) {
       //ACUMULADOR:
       //++  --  +=  -= *= /=
       int cuenta = 0;
       // ASIGNACIÃ“N --> =
        System.out.println(cuenta);
       cuenta = cuenta + 100; //acumulador
        System.out.println(cuenta);
       cuenta = cuenta + 1000;
        System.out.println(cuenta);
       cuenta = cuenta - 800; //cuenta-=
        System.out.println(cuenta);
        cuenta += 2000; //cuenta = cuenta + 2000;
        System.out.println(cuenta);
    }
    
}
